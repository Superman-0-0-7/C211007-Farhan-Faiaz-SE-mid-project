function redirectTo(option) {
    if (option === 'todo') {
      window.location.href = 'todo.html';  // Redirect to your To-Do List page
    } else if (option === 'bmi') {
      window.location.href = 'bmi.html';  // Redirect to your BMI Calculator page
    } else if (option === 'binary') {
      window.location.href = 'binary.html';  // Redirect to your Binary to Decimal Converter page
    }
  }
  

 

